#!/bin/bash

max=7
i=0
r=0
g=0
b=0

while [ 1 ]
do
	#Red
	for ((i=0; i<=$max; ++i )) ; 
	do
		r=$i
		g=0
		b=0
		./fillcolor $r $g $b
	done
	
	#Green
	for ((i=0; i<=$max; ++i )) ; 
	do
		r=0
		g=$i
		b=0
		./fillcolor $r $g $b
	done
	
	#Blue
	for ((i=0; i<=$max; ++i )) ; 
	do
		r=0
		g=0
		b=$i
		./fillcolor $r $g $b
	done

	#Yellow
	for ((i=0; i<=$max; ++i )) ; 
	do
		r=$i
		g=$i
		b=0
		./fillcolor $r $g $b
	done

	#Magenta
	for ((i=0; i<=$max; ++i )) ; 
	do
		r=$i
		g=0
		b=$i
		./fillcolor $r $g $b
	done

	#Cyan
	for ((i=0; i<=$max; ++i )) ; 
	do
		r=0
		g=$i
		b=$i
		./fillcolor $r $g $b
	done

	#Orange
	for ((i=0; i<=$max; ++i )) ; 
	do
		r=$i
		g=3
		b=0
		./fillcolor $r $g $b
	done
done



